public class Arrays {


    public static void main(String[] args) {

        int[] intArray = {19, 2, 21, 44, 13, 99};

        System.out.println(intArray[0] + " " + intArray[2] + " ");
        System.out.println(intArray[0] + intArray[2]);
        System.out.println(intArray[2] + intArray[3]);
        System.out.println(intArray.length);
    }
}